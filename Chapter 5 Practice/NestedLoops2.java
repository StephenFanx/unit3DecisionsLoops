public class NestedLoops2
{
    public static void main(String[] args)
    {
        for(int leftCol = 1; leftCol <= 2; leftCol++)
        {
            for (int rightCol = 1; rightCol <= 4; rightCol++)
            {
                System.out.println(leftCol + " " + rightCol);
            }
        }
    }
}    
            